package loops;

public class IntroLoop {
    public static void main(String[] args) {

        for (int i = -5; i <= 5; i++){
            System.out.println(i);
        }
        for (int b = 1; b <= 20; b++){
            System.out.println("Apples");
        }
        for (int c = 1; c <= 10; c++){
            System.out.println(2 + " * " + c + " = " + (2 * c));
        }
        for (int d = 1; d <= 5; d++){
            System.out.println("Java is fun");
        }

    }
}
